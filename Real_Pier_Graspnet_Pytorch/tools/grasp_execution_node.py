#!/usr/bin/env python3
"""
Isaac Sim Grasp Execution Node

This ROS2 node runs on the remote machine (192.168.100.12) and executes
grasp commands generated by Contact-GraspNet.

It subscribes to grasp execution topics, transforms camera-frame poses to
robot base coordinates, and executes the pregrasp -> grasp -> retreat
motion sequence using MoveIt IK service.

Architecture:
1. Subscribe to /piper/grasp_execution (JSON) or individual pose topics
2. Get current end effector pose from /piper/end_pose
3. Transform camera-frame grasp poses to base_link/world frame
4. Call MoveIt IK service (/compute_ik) to get joint angles
5. Publish joint commands to /piper/joint_ctrl for execution

Coordinate Transform Chain:
- Grasp pose in camera frame -> transform to gripper_base frame -> transform to world frame
- Camera relative to gripper_base: translation=[-0.045, 0.0, 0.065], euler=[0, -90, 0]
- Robot base/end-effector world transforms are read from live ROS topics / TF

Usage:
    python3 grasp_execution_node.py

Requirements (on remote machine):
    - ROS2 (Humble/Jazzy)
    - MoveIt2 with IK service running
    - Isaac Sim with Piper robot simulation
"""

